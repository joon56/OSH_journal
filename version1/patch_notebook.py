    "# F: 경영진 안전 선언·소통 채널 (Q17_1~6 / Q16_1~6) — 의례적 형식 신호\n",
    "# S: 실제 운영 시스템·행동·효과 (Q17_7~17 / Q16_7~18) — 실질 작동 신호\n",
