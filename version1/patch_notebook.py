def get_cell(nb, cell_id):
    for cell in nb['cells']:
        if cell.get('id') == cell_id:
            return cell
    raise KeyError(f'cell {cell_id} not found')

def set_source(cell, lines_list):
    cell['source'] = lines_list
    cell['outputs'] = []
    cell['execution_count'] = None

