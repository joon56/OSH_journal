    "# 제도적 형식주의 통제변수 (SDI와 독립적인 인증·위원회 이진 변수)\n",
    "reg_con['formal_inst'] = reg_con[['Q6','Q9','Q10','Q12_1','Q12_2']].mean(axis=1)\n",
