    "    'S-M_행동준수':  ['Q17_14','Q17_15','Q17_16','Q17_17'],\n",
    "}\n",
    "\n",
    "F_CON_ALL = [c for v in F_CON_GROUPS.values() for c in v]\n",
    "S_CON_ALL = [c for v in S_CON_GROUPS.values() for c in v]\n",
