    "print(f'F 변수 수: {len(F_MS_ALL)}, S 변수 수: {len(S_MS_ALL)}')\n",
    "print('-> SDI = S_score - F_score (운영실질 - 경영선언)',\n",
    "      '| SDI<0: 선언>실질 = 형식주의 | SDI>0: 실질>선언 = 진짜 안전')",
