    "        reg_con = pd.concat([reg_con, dummies], axis=1)\n",
    "\n",
    "count_vars_con = ['SDI', 'formal_inst', 'edu_impl', 'log_safecost',\n",
    "                   'ratio_aged', 'ratio_foreign', 'ratio_female']\n",
